// for loop = repeat some code a certain amount of times.
// first statment, second is condition and will continue statment as log as this condition is true
// third stament incrimates first statment.

for (let i = 1; i <= 10; i += 1) {
  console.log(i);
}
console.log("Done");

// count down.

for (let i = 10; i > 0; i -= 1) {
  console.log(i);
}
console.log("Done");
